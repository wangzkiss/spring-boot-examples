package cn.vigor.API.model.host;

public class HostComponentItems {
	
	private String href;
	private HostRoles HostRoles;
	private Host host;
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public HostRoles getHostRoles() {
		return HostRoles;
	}
	public void setHostRoles(HostRoles hostRoles) {
		HostRoles = hostRoles;
	}
	public Host getHost() {
		return host;
	}
	public void setHost(Host host) {
		this.host = host;
	}
	
	
	

}
